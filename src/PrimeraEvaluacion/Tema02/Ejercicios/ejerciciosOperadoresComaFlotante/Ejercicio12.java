package PrimeraEvaluacion.Tema02.Ejercicios.ejerciciosOperadoresComaFlotante;

public class Ejercicio12 {
    public static void main(String[] args) {
        /*
        Realiza un programa que calcule el área de una circunferencia de radio 5,2 centímetros
         */
        double radio = 5.2;
        double resultado = Math.PI * radio * radio;
        System.out.println("El área de una circunferencia de 5,2 centímetros es: " + resultado + " centímetros cuadrados");
    }
}
